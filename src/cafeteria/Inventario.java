     void MostrarCupones(){   
     String[] titu= { "IdCupon","NombreCup", "Descuento", "Validez"};
     String[] registros;
        registros = new String[5];
     
     DefaultTableModel modelo= new DefaultTableModel(null,titu);
     
     String sql = "select * FROM cupones";
     
        try {
           Statement st= cn.createStatement();
           rs= st.executeQuery(sql);
           
           while(rs.next())
           {
              registros[0]= rs.getString("IdCupon");
               registros[1]= rs.getString("NombreCup");
               registros[2]= rs.getString("Descuento");
               registros[3]= rs.getString("Validez");
              
               
               modelo.addRow(registros);
           }
           
          jTable1.setModel(modelo);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar datos"+e.getMessage());
        }
    }
    
    
    
