    void mostrarDatos (String identificador){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Fecha Adqu.");
        modelo.addColumn("Tipo");
        modelo.addColumn("Stock");
        jTableAct.setModel(modelo);
        String sql="";
        if(identificador.equals("")){
            sql="SELECT * FROM inventario";
        }
        else{
            sql="SELECT * FROM inventario WHERE idInv='"+identificador+"'";
        }
        
        String []datos =  new String[5];
        
        try {
            Statement dst= cn.createStatement();
            ResultSet rs = dst.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                modelo.addRow(datos);
            }
            jTableAct.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
